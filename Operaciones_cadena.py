# Operaciones con Strings

# Concatenar con el signo +
nombre = "Maria"
apellido = "Perez"

nombre_completo = nombre + " " + apellido
print(nombre_completo)


# Concatenar con el signo *
cadena = "Colombia "
multiplicar_cadena = cadena * 6
print(multiplicar_cadena)

#Métodos de los Strings

# x.upper()
# Este método devuelve una copia de la cadena convirtiendo
# las letras minusculas a mayusculas

frase = "Curso de Python"
print(frase.upper())

#x.lower()
frase = "CURSO DE PYTHON"
print(frase.lower())
